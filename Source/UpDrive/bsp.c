/*********************************************************************************************************
    模块名称 : BSP模块(For STM32F1XX)
    文件名称 : bsp.c
    说    明 : 这是硬件底层驱动程序模块的主文件。主要提供 bsp_Init()函数供主程序调用。
               主程序的每个c文件可以在开头添加 #include "bsp.h" 来包含所有的外设驱动模块。
*********************************************************************************************************/
#include "bsp.h"
/*********************************************************************************************************
    函 数 名: bsp_Init
    功能说明: 初始化硬件设备。只需要调用一次。该函数配置CPU寄存器和外设的寄存器并初始化一些全局变量。
             全局变量。
    形    参：无
    返 回 值: 无
*********************************************************************************************************/
void bsp_Init ( void )
{
	/* 优先级分组设置为4 */
//	NVIC_PriorityGroupConfig ( NVIC_PriorityGroup_4 ); //初始化中断优先级
	LED_Init(); //初始化Led的GPIO
//    bsp_POWER_Init(); //初始化一键开关机电路的IO口
    
	/*初始化定时器*/
	SysTickTimer_Init();    //初始化系统滴答定时器 (此函数会开中断)
	bsp_HardTimerInit();    //初始化硬件定时器 
	/*以下为开启SPI驱动方式的OLED*/    
    OLEDIO_Init(); 
	SPI1_Init();    //初始化SPI
	LCD_DLY_ms ( 500 );
	OLED_Init();  //初始化OLed的GPIO并配置
	OLED_CLS();
	OLED_Fill ( 0x00 );
//	LCD_P6x8Str ( 0, 0, "OLED Test" );
	/*以下为开启3个串口*/
	UART_InitALL(); //初始化串口
    
//	ADC_Configuration(); //初始化ADC0

//	初始化SX1278
    RFGPIOInit();
	RFInit();    //射频模块初始化
	RFRxMode();  //进入接收模式

//	初始化AD5933
    I2C_EE_Init();
    Init_AD5933();
    
//  初始化多路选择器    
    bsp_pztMux_Init();
    bsp_rfbMux_Init(); 
    
}

